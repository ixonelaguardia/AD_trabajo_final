    public void opcion(String o) {
        this.opcion = o.toUpperCase();
        switch (this.opcion) {
            case "PROVEEDOR":
                this.setTitle("Proveedores");
                // campos proyecto
                ocultarProyecto();
                // campos pieza
                ocultarPieza();
                break;
            case "PIEZA":
                this.setTitle("Piezas");
                // campos proveedor
                ocultarProveedor();
                // campos proyecto
                ocultarProyecto();
                break;
            case "PROYECTO":
                this.setTitle("Proyectos");
                // campos proveedor
                ocultarProveedor();
                // campos pieza
                ocultarPieza();
                break;
            default:
                break;
        }
    }

