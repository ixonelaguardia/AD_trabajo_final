    public void opcion(String o) throws SQLException {
        this.opcion = o.toUpperCase();
        switch (this.opcion) {
            case "PROVEEDOR":
                this.setTitle("Proveedores");
                // campos proyecto
                ocultarProyecto();
                // campos pieza
                ocultarPieza();
                // obtener datos
                obtenerProveedores();
                break;
            case "PIEZA":
                this.setTitle("Piezas");
                // campos proveedor
                ocultarProveedor();
                // campos proyecto
                ocultarProyecto();
                // obtener datos
                obtenerPiezas();
                break;
            case "PROYECTO":
                this.setTitle("Proyectos");
                // campos proveedor
                ocultarProveedor();
                // campos pieza
                ocultarPieza();
                // obtener datos
                obtenerProyectos();
                break;
            default:
                break;
        }
    }

