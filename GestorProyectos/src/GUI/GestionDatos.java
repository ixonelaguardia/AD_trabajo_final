    private void iniciarControles(String o, int indice) {
        switch (o) {
            case "PROVEEDOR":
                // DATOS DE LA PESTAÑA 2 QUE SE MOSTRARÁN POR DEFECTO (el primer registro)
                Proveedor p = lista_proveedores.get(indice);
                tf_codigo1.setText(p.getCodigo());
                tf_nombre1.setText(p.getNombre());
                tf_ape1.setText(p.getApellido());
                tf_direc1.setText(p.getDireccion());
                break;
            case "PIEZA":
                // DATOS DE LA PESTAÑA 2 QUE SE MOSTRARÁN POR DEFECTO (el primer registro)
                Pieza pi = lista_piezas.get(indice);
                tf_codigo1.setText(pi.getCodigo());
                tf_nombre1.setText(pi.getNombre());
                tf_precio1.setText(String.valueOf(pi.getPrecio()));
                tf_descrip1.setText(pi.getDescripcion());

                break;
            case "PROYECTO":
                // DATOS DE LA PESTAÑA 2 QUE SE MOSTRARÁN POR DEFECTO (el primer registro)
                Proyecto po = lista_proyectos.get(indice);
                tf_codigo1.setText(po.getCodigo());
                tf_nombre1.setText(po.getNombre());
                tf_ciudad1.setText(po.getCiudad());
                break;
            default:
                break;
        }
    }

    private void obtenerRegistro() {

        switch (this.opcion) {
            case "PROVEEDOR":
                // DATOS DE LA PESTAÑA 1 si encuentre el codigo
                for (Proveedor p : lista_proveedores) {
                    if (p.getCodigo().equalsIgnoreCase(tf_codigo.getText())) {
                        tf_nombre.setText(p.getNombre());
                        tf_ape.setText(p.getApellido());
                        tf_direc.setText(p.getDireccion());
                        break;
                    }
                }

                break;
            case "PIEZA":
                // DATOS DE LA PESTAÑA 1 si encuentre el codigo
                for (Pieza p : lista_piezas) {
                    if (p.getCodigo().equalsIgnoreCase(tf_codigo.getText())) {
                        tf_nombre.setText(p.getNombre());
                        tf_precio1.setText(String.valueOf(p.getPrecio()));
                        tf_descrip1.setText(p.getDescripcion());
                        break;
                    }
                }
                break;
            case "PROYECTO":
                // DATOS DE LA PESTAÑA 1 si encuentre el codigo
                for (Proyecto p : lista_proyectos) {
                    if (p.getCodigo().equalsIgnoreCase(tf_codigo.getText())) {
                        tf_nombre.setText(p.getNombre());
                        tf_ciudad1.setText(p.getCiudad());
                        break;
                    }
                }

                break;
            default:
                break;
        }
    }

    private int eliminarRegistro(String codigo) throws SQLException {

        int filas = 0;
        switch (this.opcion) {
            case "PROVEEDOR":
                for (Proveedor p : lista_proveedores) {
                    if (p.getCodigo().equalsIgnoreCase(codigo)) {

                        String sql = "DELETE FROM proveedor WHERE codigo = ?";
                        PreparedStatement sentencia = con.prepareStatement(sql);
                        sentencia.setString(1, codigo);
                        filas = sentencia.executeUpdate();
                        if (filas > 0) {
                            lista_proveedores.remove(p);
                        }
                        break;
                    }
                }
                break;
            case "PIEZA":
                for (Pieza p : lista_piezas) {
                    if (p.getCodigo().equalsIgnoreCase(codigo)) {
                        String sql = "DELETE FROM pieza WHERE codigo = ?";
                        PreparedStatement sentencia = con.prepareStatement(sql);
                        sentencia.setString(1, codigo);
                        filas = sentencia.executeUpdate();
                        if (filas > 0) {
                            lista_piezas.remove(p);
                        }
                        break;
                    }
                }
                break;
            case "PROYECTO":
                for (Proyecto p : lista_proyectos) {
                    if (p.getCodigo().equalsIgnoreCase(codigo)) {
                        String sql = "DELETE FROM proyecto WHERE codigo = ?";
                        PreparedStatement sentencia = con.prepareStatement(sql);
                        sentencia.setString(1, codigo);
                        filas = sentencia.executeUpdate();
                        if (filas > 0) {
                            lista_proyectos.remove(p);
                        }
                        break;
                    }
                }
                break;
            default:
                break;
        }
        return filas;
    }
    
